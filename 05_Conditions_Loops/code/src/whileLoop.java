
//While Loop :
/*
    Syntax :
        while (condition){
             // code to be executed
             // increment/decrement
        }
 */

public class whileLoop {
    public static void main(String[] args) {
        int num = 1;
        while (num <=5){
            System.out.println(num);
            num += 1;
        }
    }
}
