// Do While Loop
/*
    Syntax :
        do{
            // code to be executed
            // update statement -> increment/decrement
        }while (condition);
 */

public class doWhileLoop {
    public static void main(String[] args) {
        int n = 1;
        do{
            System.out.println(n);
            n++;
        } while(n>=5);
    }
}
